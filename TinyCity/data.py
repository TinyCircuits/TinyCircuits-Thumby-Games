def terrain_bytes_to_map(data_bytes, width=48, height=48):
    terrain = []
    blocks_per_row = width // 8
    for y in range(height):
        row = bytearray(width)
        block_y = y >> 3
        block_v = y & 7
        for x in range(width):
            block_x = x >> 3
            block_u = x & 7
            index = (block_y * blocks_per_row + block_x) * 8 + block_u
            mask = 1 << block_v
            is_land = (data_bytes[index] & mask) != 0
            row[x] = 0 if is_land else 1
        terrain.append(row)
    return terrain


